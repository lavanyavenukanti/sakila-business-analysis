-- Question 2
-- Business Question- How the Top 5 customers in each store based on total revenue.

with customer_revenue as
(select customer.customer_id, customer.store_id, sum(amount) as total_revenue
from payment
inner join customer
on payment.customer_id = customer.customer_id
group by customer_id,store_id),
store_rank as 
(select store_id, customer_id, total_revenue,
row_number() over
(partition by store_id
order by total_revenue desc)
as row_num
from customer_revenue)
select customer.store_id, customer.customer_id, first_name, last_name,
total_revenue, row_num
from store_rank
inner join customer
on store_rank.customer_id = customer.customer_id
where row_num <= 5;
