-- Question 7
-- Business Question- Show the Top 3 films within each film rating (G, PG, PG-13, R, etc.) 
-- based on rental count.

with film_rentals as
(select title, rating, count(rental_id) as total_rentals
from rental
inner join inventory
on rental.inventory_id = inventory.inventory_id
inner join film
on inventory.film_id = film.film_id
group by title, rating),
film_ranking as
(select title, rating, total_rentals,
row_number() over(partition by rating
order by total_rentals desc) as rank_row
from film_rentals)
select title, rating, total_rentals, rank_row
from film_ranking
where rank_row <= 3;
