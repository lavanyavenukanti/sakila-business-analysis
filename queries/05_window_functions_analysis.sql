-- Question 4
-- Business Question- For each month, calculate the total revenue and rank the months 
-- from highest to lowest revenue.

with monthly_revenue as
(select sum(amount) as total_revenue, monthname(payment_date) as month_name
from payment
group by month_name),
monthly_rank as
(select total_revenue, month_name,
row_number() over(order by total_revenue desc) as rank_row
from monthly_revenue)
select month_name, total_revenue, rank_row
from monthly_rank;
