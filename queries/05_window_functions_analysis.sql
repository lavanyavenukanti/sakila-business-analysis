-- Question 1
-- Business Question- Which customers generated the highest total revenue for the company?
-- Show the Top 10 customers along with their names and total revenue.

select customer.customer_id, first_name, last_name, sum(amount) as
total_amount
from customer
inner join payment
on customer.customer_id = payment.customer_id
group by customer_id, first_name, last_name
order by total_amount desc
limit 10;
