-- =====================================================
-- Question 3
-- Business Question- Find customers whose total revenue is above the average customer revenue.

with customer_revenue as
(select customer_id, 
sum(amount) as total_revenue
from payment
group by customer_id)
average_revenue as
(select avg(total_revenue) as avg_revenue
from customer_revenue)
select customer_id, total_revenue
from customer_revenue
where total_revenue >
(select avg_revenue from average_revenue)
order by total_revenue desc;
