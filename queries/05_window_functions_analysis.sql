-- Project Question 11
-- Business Question: Calculate the running monthly revenue.
with monthly_revenue as
(select monthname(payment_date) as month_name, month(payment_date) as month_number,
sum(amount) as total_revenue
from payment
group by month(payment_date),
         monthname(payment_date))
select month_number, month_name, total_revenue, sum(total_revenue)
over (order by month_number) as running_revenue
from monthly_revenue;
