-- Project Question 12
-- Business Question: Compare each month's revenue with the previous month's revenue.
with monthly_revenue as
(select month(payment_date) as month_number,
monthname(payment_date) as month_name,
sum(amount) as total_revenue
from payment
group by month(payment_date), monthname(payment_date))
select month_number, month_name, total_revenue,
lag(total_revenue) over(order by month_number) as previous_month_revenue
from monthly_revenue;
